const getCookieValue = (name: string): string | null => {
  try {
    const match = document.cookie.split(';').find((c) => c.trim().startsWith(name + '='))
    return match ? decodeURIComponent(match.trim().split('=')[1]) : null
  } catch {
    return null
  }
}

